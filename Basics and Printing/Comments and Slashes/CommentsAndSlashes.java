// slashes in java signify comments. // allow you to make comments
// another double slashes will let you wrap text

public class CommentsAndSlashes {
	public static void main(String[] args) {
		// A comment, this is so you can read your program later.
		// Anything after the // is ignored by Java.

		System.out.println("First println test"); // and the comment after is ignored.

		// You can also use a comment to "disable" or comment out a piece of code:
		// System.out.println("This won't run.");

		System.out.println("Second \"P R I N T B O Y Z\" test tho");
	}
}